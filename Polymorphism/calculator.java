package Polymorphism;

public class calculator {

    // Polymorphism has two types: Complie-Time; Run-Time
    // Compile-Time is also known as STATIC POLYMORPHISM and of two types:
    // Method-Overloading; Operator-Overloading

    // Method-Overloading
    int add(int a, int b) {
        return a + b;
    }

    // Overloading
    int add(int a, int b, int c) {
        return a + b + c;
    }

}
